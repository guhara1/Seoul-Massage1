def _seed(slug):
    # slug 문자 위치별 가중치를 둬 비슷한 slug 도 다른 해시가 나오도록 한다.
    return sum((i + 1) * ord(c) for i, c in enumerate(slug))


def _pick(options, slug, salt):
    """slug + salt 로 결정되는 인덱스의 변형을 고른다(페이지마다 다른 조합)."""
    return options[(_seed(slug) + salt * 31 + len(slug) * 7) % len(options)]


def _josa_fix(text):
    """앞 글자의 받침 유무에 따라 'X을(를)' 형태의 조사를 올바른 하나로 정리한다."""
    import re as _re

    def has_final(ch):
        code = ord(ch)
        if 0xAC00 <= code <= 0xD7A3:
            return (code - 0xAC00) % 28 != 0
        # 한글 음절이 아니면(숫자·영문 등) 받침 있는 것으로 간주
        return True

    pairs = {"을": "를", "은": "는", "이": "가", "과": "와"}
    for fin, nofin in pairs.items():
        pattern = _re.compile(r"(.)" + fin + r"\(" + nofin + r"\)")
        text = pattern.sub(
            lambda m: m.group(1) + (fin if has_final(m.group(1)) else nofin), text
        )
    return text


def _lm(landmarks):
    parts = [p.strip() for p in landmarks.split(",") if p.strip()]
    while len(parts) < 3:
        parts.append(parts[-1])
    return parts[0], parts[1], parts[2]


