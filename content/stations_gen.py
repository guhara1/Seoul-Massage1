def _has_batchim(ch):
    c = ord(ch)
    if 0xAC00 <= c <= 0xD7A3:
        return (c - 0xAC00) % 28 != 0
    return True  # 한글 음절이 아니면 받침 있는 것으로 간주


def _fix_particles(text, *words):
    """주어진 단어 뒤의 조사(은/는·을/를·이/가·과/와)를 받침에 맞춰 교정한다.
    조사 선택은 순수 음운 규칙이므로 단어+조사 치환은 의미상 항상 안전하다."""
    for w in words:
        if not w:
            continue
        correct_a = w[-1]
        bat = _has_batchim(correct_a)
        for a, b in (("은", "는"), ("을", "를"), ("이", "가"), ("과", "와")):
            right = a if bat else b
            text = text.replace(w + a, w + right).replace(w + b, w + right)
    return text


def _seed(s):
    return sum((i + 1) * ord(c) for i, c in enumerate(s))


def _pick(options, slug, salt):
    return options[(_seed(slug) + salt * 31 + len(slug) * 7) % len(options)]


